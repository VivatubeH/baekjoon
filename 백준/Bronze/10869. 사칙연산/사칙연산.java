import java.io.*;
import java.util.*;

public class Main {
    public static void main(String[] args) throws IOException {
       Scanner scanner = new Scanner(System.in);
       int A = scanner.nextInt();
       int B = scanner.nextInt();
       System.out.println(A + B);
       System.out.println(A - B);
       System.out.println(A * B);
       System.out.println(A / B);
       System.out.println(A % B);
    }
}
